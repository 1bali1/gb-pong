INCLUDE "hardware.inc"

SECTION "Header", ROM0[0x100]
    jp EntryPoint

    ds 0x150 - @, 0

EntryPoint:
    xor a
    ld [rNR52], a

FirstVBlank:
    ld a, [rLY]
    cp 144
    jr c, FirstVBlank 

    xor a
    ld [rLCDC], a

    call ClearOAM

    ; load tiles
    ld de, Tiles
    ld hl, 0x9000
    ld bc, Tiles.End - Tiles

    call CopyTo

    ; load tilemap
    ld de, Tilemap
    ld hl, 0x9800
    ld bc, Tilemap.End - Tilemap

    call CopyTo

    ; load objects
    ld de, Paddle
    ld hl, 0x8000
    ld bc, Paddle.End - Paddle

    call CopyTo

    ld de, Ball
    ld hl, 0x8017 
    ld bc, Ball.End - Ball

    call CopyTo

    ; initialize oam
    call InitOAM

    ; initalize objects
    call InitObjects

    ld a, LCDC_ON | LCDC_OBJ_ON | LCDC_BG_ON
    ld [rLCDC], a

    ld a, 0b11100100
    ld [rBGP], a
    ld [rOBP0], a

    xor a
    ld [wFrameCounter], a
    ld [wCurrentKeys], a
    ld [wNewKeys], a

Main:
    ld a, [rLY]
    cp 144
    jp nc, Main

WaitVBlank:
    ld a, [rLY]
    cp 144
    jp c, WaitVBlank

/*     ld a, [wFrameCounter]
    inc a
    ld [wFrameCounter], a
    cp a, 5
    jp nz, Main */

    ld a, [wBallMomentumX]
    ld b, a
    ld a, [STARTOF(OAM) + 5]
    add a, b
    ld [STARTOF(OAM) + 5], a

    ld a, [wBallMomentumY]
    ld b, a
    ld a, [STARTOF(OAM) + 4]
    add a, b
    ld [STARTOF(OAM) + 4], a

    call UpdateKeys

BounceOnTop:
    ld a, [STARTOF(OAM) + 5]
    sub a, 8
    ld b, a

    ld a, [STARTOF(OAM) + 4]
    sub a, 16 + 1 ; offset + check above
    ld c, a

    call GetTileByPixel
    ld a, [hl]

    call CanBounceFromWall

    jp nz, BounceOnRight

    ld a, 1
    ld [wBallMomentumY], a

BounceOnRight:
    ld a, [STARTOF(OAM) + 5]
    sub a, 8 - 1
    ld b, a

    ld a, [STARTOF(OAM) + 4]
    sub a, 16
    ld c, a

    call GetTileByPixel
    ld a, [hl]

    call CanBounceFromWall

    jp nz, BounceOnLeft

    ld a, -1
    ld [wBallMomentumX], a

BounceOnLeft:
    ld a, [STARTOF(OAM) + 5]
    sub a, 8 + 1
    ld b, a

    ld a, [STARTOF(OAM) + 4]
    sub a, 16
    ld c, a

    call GetTileByPixel
    ld a, [hl]

    call CanBounceFromWall

    jp nz, BounceOnBottom

    ld a, 1
    ld [wBallMomentumX], a

BounceOnBottom:
    ld a, [STARTOF(OAM) + 5]
    sub a, 8
    ld b, a

    ld a, [STARTOF(OAM) + 4]
    sub a, 16 - 1
    ld c, a

    call GetTileByPixel
    ld a, [hl]

    call CanBounceFromWall

    jp nz, BounceDone

    ld a, -1
    ld [wBallMomentumY], a

BounceDone:

CheckLeft:
    ld a, [wCurrentKeys]
    and a, PAD_LEFT

    jp z, CheckRight

MoveLeft:
    ld a, [STARTOF(OAM) + 1]
    dec a
    dec a

    cp 8
    jp z, Main

    ld [STARTOF(OAM) + 1], a

    jp Main

CheckRight:
    ld a, [wCurrentKeys]
    and a, PAD_RIGHT

    jp z, Main

MoveRight:
    ld a, [STARTOF(OAM) + 1]
    inc a
    inc a

    cp 160
    jp z, Main

    ld [STARTOF(OAM) + 1], a

    jp Main


; @param de: source
; @param hl: dest
; @param bc: length
CopyTo:
    ld a, [de]
    ld [hl+], a

    inc de
    dec bc

    ld a, b
    or c
    jr nz, CopyTo

    ret

; clean function
ClearOAM:
    ld a, 0
    ld b, 160
    ld hl, STARTOF(OAM)

.clearOam:
    ld [hl+], a
    dec b
    jr nz, .clearOam

    ret

; clean function
InitOAM:
    ld hl, STARTOF(OAM)
    
    ; paddle
    ; y pos
    ld a, 128 + 16
    ld [hl+], a

    ; x pos
    ld a, 16 + 8
    ld [hl+], a

    ; tile index & attributes
    xor a
    ld [hl+], a
    ld [hl+], a

    ; ball
    ; y pos
    ld a, 100 + 16
    ld [hl+], a

    ; x pos
    ld a, 50 + 8
    ld [hl+], a

    ; tile index & attributes
    ld a, 1
    ld [hl+], a
    xor a
    ld [hl+], a

    ret

InitObjects:
    ld a, 1
    ld [wBallMomentumX], a

    ld a, -1
    ld [wBallMomentumY], a

UpdateKeys:
    ; update buttons
    ld a, JOYP_GET_BUTTONS
    call .pollKeys
    ld b, a

    ; update dirs
    ld a, JOYP_GET_CTRL_PAD
    call .pollKeys

    swap a
    xor b
    ld b, a

    ld a, JOYP_GET_NONE
    ldh [rJOYP], a

    ld a, [wCurrentKeys]
    xor b
    and b
    ld [wNewKeys], a

    ld a, b
    ld [wCurrentKeys], a

    ret

; @param a: key matrix
.pollKeys:
    ldh [rJOYP], a
    call .wait
    
    ldh a, [rJOYP]
    ldh a, [rJOYP]
    ldh a, [rJOYP]

    or 0xf0

.wait:
    ret

; @param b: x
; @param c: y
; @return hl: tile address
GetTileByPixel:
    ; y
    ld a, c
    and a, 0b11111000 ; it should be dividable by 8
    ld l, a
    ld h, 0

    add hl, hl
    add hl, hl

    ; divide by 8 -> x
    ld a, b
    srl a
    srl a
    srl a

    ; add them together
    ; l = y
    ; a = x

    ; low
    add a, l
    ld l, a

    ; high
    adc a, h
    sub a, l
    ld h, a

    ld bc, 0x9800
    add hl, bc
    
    ret

; @param a: tile id
; @return Z: if it can bounce from the wall, set
CanBounceFromWall:
    cp a, 0x03
    ret z
    cp a, 0x04
    ret z
    cp a, 0x05
    ret z
    cp a, 0x06
    ret z
    cp a, 0x07
    ret z
    cp a, 0x08
    ret z
    cp a, 0x09
    ret z
    cp a, 0x0a
    ret

Done:
    jr Done

