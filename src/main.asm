INCLUDE "hardware.inc"

SECTION "Header", ROM0[0x100]
    jp EntryPoint

    ds 0x150 - @, 0

EntryPoint:
    xor a
    ld [rNR52], a

FirstVBlank:
    ld a, [rLY]
    cp 144
    jr c, FirstVBlank 

    xor a
    ld [rLCDC], a

    call ClearOAM

    ; load tiles
    ld de, Tiles
    ld hl, 0x9000
    ld bc, Tiles.End - Tiles

    call CopyTo

    ; load tilemap
    ld de, Tilemap
    ld hl, 0x9800
    ld bc, Tilemap.End - Tilemap

    call CopyTo

    ; load objects
    ld de, Paddle
    ld hl, 0x8000
    ld bc, Paddle.End - Paddle

    call CopyTo

    ; write padde to oam
    call PaddleToOAM

    ld a, LCDC_ON | LCDC_OBJ_ON | LCDC_BG_ON
    ld [rLCDC], a

    ld a, 0b11100100
    ld [rBGP], a
    ld [rOBP0], a

    xor a
    ld [wFrameCounter], a

Main:
    ld a, [rLY]
    cp 144
    jp nc, Main

WaitVBlank:
    ld a, [rLY]
    cp 144
    jp c, WaitVBlank

    ld a, [wFrameCounter]
    inc a
    ld [wFrameCounter], a
    cp a, 5
    jp nz, Main

    xor a
    ld [wFrameCounter], a

    ld a, [STARTOF(OAM) + 1]
    inc a
    ld [STARTOF(OAM) + 1], a

    jp Main

CopyTo:
    ld a, [de]
    ld [hl+], a

    inc de
    dec bc

    ld a, b
    or c
    jr nz, CopyTo

    ret

ClearOAM:
    ld a, 0
    ld b, 160
    ld hl, STARTOF(OAM)

.clearOam:
    ld [hl+], a
    dec b
    jr nz, .clearOam

    ret

PaddleToOAM:
    ld hl, STARTOF(OAM)
    
    ; y pos
    ld a, 128 + 16
    ld [hl+], a

    ; x pos
    ld a, 16 + 8
    ld [hl+], a

    ; attr zero & tileindex
    xor a
    ld [hl+], a
    ld [hl+], a

    ret
    
Done:
    jr Done

