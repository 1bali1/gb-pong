INCLUDE "hardware.inc"

SECTION "Header", ROM0[0x100]
    jp EntryPoint

    ds 0x150 - @, 0

EntryPoint:
    ld a, 0
    ld [rNR52], a

WaitVBlank:
    ld a, [rLY]
    cp 144
    jr c, WaitVBlank

    ld a, 0
    ld [rLCDC], a

    ld de, Tiles
    ld hl, 0x9000
    ld bc, Tiles.End - Tiles

CopyTiles:
    ld a, [de]
    ld [hl+], a

    inc de
    dec bc

    ld a, b
    or c
    jr nz, CopyTiles

    ld de, Tilemap
    ld hl, 0x9800
    ld bc, Tilemap.End - Tilemap

CopyTilemap:
    ld a, [de]
    ld [hl+], a

    inc de
    dec bc

    ld a, b
    or c
    jr nz, CopyTilemap

    ld a, LCDC_ON | LCDC_BG_ON
    ld [rLCDC], a

    ld a, 0b11100100
    ld [rBGP], a

Done:
    jr Done


